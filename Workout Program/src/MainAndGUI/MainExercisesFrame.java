    private void exFileBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exFileBtnActionPerformed
        try {
            AllExercisesSys.readExTxt("exlist.txt");
            if (AllExercisesSys.list.isEmpty()) {
                outputTA.setText("All Exercises File Not Read!");
            } else {
                outputTA.setText("All Exercises File is Read!");
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainExercisesFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_exFileBtnActionPerformed

