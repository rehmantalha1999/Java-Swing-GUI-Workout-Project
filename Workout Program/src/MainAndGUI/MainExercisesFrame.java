    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (searchTA.getText().isEmpty()) {
            outputTA.setText("Enter the name of the Exercise to Search!");
        } else {
            if (AllExercisesSys.searchExercise(searchTA.getText()) == null) {
                outputTA.setText("Exercise Not Found!");
            } else {
                outputTA.setText(AllExercisesSys.searchExercise(searchTA.getText()).toString());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

