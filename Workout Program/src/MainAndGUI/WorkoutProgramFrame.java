    private void selectExerciseCbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_selectExerciseCbMouseClicked
        if (selectExerciseCb.getItemCount() == 0) {
            String[] exNames = AllExercisesSys.getAllExercisesNames();
            for (int i = 0; i < exNames.length; i++) {
                selectExerciseCb.addItem(exNames[i]);
            }
        }
    }//GEN-LAST:event_selectExerciseCbMouseClicked

    private void dispBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dispBtnActionPerformed
        if (ExerciseSys.SelectedExList.isEmpty()) {
            programOutputTA.setText("You have not added any exercises to your your Workout Program!");
        } else {
            programOutputTA.setText("List of all your selected exercises with details and strength Plan for next week:\n\n" + ExerciseSys.getAllSelectedExercises());
        }

    }//GEN-LAST:event_dispBtnActionPerformed

    private void removeCBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeCBMouseClicked
        if (removeCB.getSelectedItem() == null) {
            String[] exNames = ExerciseSys.getAllExercisesNames();
            for (int i = 0; i < exNames.length; i++) {
                removeCB.addItem(exNames[i]);
            }
        }
    }//GEN-LAST:event_removeCBMouseClicked

    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        try {
            String s;
            s = (String) selectExerciseCb.getSelectedItem();
            WorkoutProgram w = AllExercisesSys.searchExercise(s);
            WorkoutProgram sm = ExerciseSys.removeExercise(w.getExerciseId());
            programOutputTA.setText(sm.getExerciseName() + " is removed from your program");
        } catch (NullPointerException ex) {
            programOutputTA.setText("No Exercise added to the program!");
        }
    }//GEN-LAST:event_removeBtnActionPerformed

