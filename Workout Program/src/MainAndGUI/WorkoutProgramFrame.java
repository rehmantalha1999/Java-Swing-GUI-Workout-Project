    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        boolean status = true;
        try {
            try {
                if (selectExerciseCb.getSelectedItem() == null) {
                    programOutputTA.setText("Select an Exercise");
                    status = false;
                }
                if (setsTextField.getSelectedText().isEmpty()) {
                    programOutputTA.setText("Enter number of sets you did recently");
                    status = false;
                }
                if (repsTextField.getSelectedText().isEmpty()) {
                    programOutputTA.setText("Enter the number of reps you did recently");
                    status = false;
                }
            } catch (NullPointerException ex) {
            }
            if (status) {
                String s;
                s = (String) selectExerciseCb.getSelectedItem();
                WorkoutProgram w = AllExercisesSys.searchExercise(s);
                String exerciseId = w.getExerciseId();
                int noOfReps = Integer.parseInt(repsTextField.getText());
                int noOfSets = Integer.parseInt(setsTextField.getText());
                int weightsLifted = Integer.parseInt(weightsTextField.getText());
                boolean stts = ExerciseSys.addExercise(exerciseId, weightsLifted, noOfSets, noOfReps);
                if (!stts) {
                    programOutputTA.setText("Exercise already exists in the program!");
                } else {
                    programOutputTA.setText("Exercise Added!\n" + ExerciseSys.getAllSelectedExercises());
                }
                repsTextField.setText("");
                setsTextField.setText("");
                weightsTextField.setText("");
            }
        } catch (NumberFormatException ex) {
            programOutputTA.setText("Either any empty fields or some fields have dont have an integer value");
        }

    }//GEN-LAST:event_addBtnActionPerformed

